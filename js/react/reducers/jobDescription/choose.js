const choose = (state = initialState, action) => {
  switch (action.type) {
    case ActionTypes.SET_SELECTED_JD:
      return {
        ...state,
        selectedJd: action.payload.id,
        selectedJdText: action.payload.text
      }

    case ActionTypes.SET_JD_TEXT:
      return { ...state, selectedJdText: action.payload }

    case ActionTypes.CLEAR_JOB_DESCRIPTION: {
      return {
        ...state,
        selectedJd: 0,
        selectedJdText: ''
      }
    }
    default:
      return state
  }
}

